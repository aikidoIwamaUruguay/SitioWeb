<p>
"En el Aikido hay muchos maestros. Vaya y v&eacute;alos, eso est&aacute; bien. Encuentre la escuela que m&aacute;s le conviene. Usted tiene esa libertad, yo no.
Yo practico el Aikido del Fundador."
</p>
<p align="right">
Morihiro Saito Sensei
</p>
