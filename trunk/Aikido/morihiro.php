<h1>Morihiro Saito Sensei</h1>

<img src="img/saito-sensei.gif" style="float:left; margin-right: 10px;" />
<p>
	Morihiro Saito Shihan 9<sup>o</sup> Dan de Aikido, naci&oacute; en la prefectura de Ibaraki, Jap&oacute;n el 31 de marzo de 1928. Fue uno de los practicantes 
	de Aikido m&aacute;s destacados y de m&aacute;s alta graduaci&oacute;n en el mundo. 
</p>
<p>
	Proveniente de una familia granjera de pocos recursos, comenz&oacute; su estudio de las artes marciales entrenando Karate (estilo Shito-ryu) en Meguro. 
	Tambi&eacute;n entren&oacute; Judo y Kendo.
</p>
<p>
	En 1946, a la edad de 18 a&#241;os, escuch&oacute; acerca de "un anciano que hac&iacute;a extra&#241;as t&eacute;cnicas en una monta&#241;a cerca de Iwama". 
	Se trataba del maestro Morihei Ueshiba, que en esa &eacute;poca ense&#241;aba "Ueshiba-ryu budo". Desde ese a&#241;o hasta la muerte de O'Sensei en 1969, Saito sirvi&oacute; de asistente del maestro Ueshiba (como Uchi-deshi), a la vez que su propia esposa servia a la mujer de O'Sensei.
</p>
<img src="img/saitoConOsensei.jpg" style="float:right; margin-left: 10px;" />
<p>
	El fundador confi&oacute; completamente en su devoto alumno y le ense&#241;&oacute; todo, ya sea desde el punto de vista te&oacute;rico como t&eacute;cnico. 
	Saito Sensei no se permiti&oacute; nunca interpretar o cuestionar lo que recibi&oacute; de O'Sensei; simplemente se limit&oacute; a memorizar, a estudiar y a 
	ayudar al Maestro. Morihiro Saito fue el &uacute;nico Maestro que preserv&oacute; y transmiti&oacute; intacto el Aikido original tal y como se 
	ense&#241;&oacute; en Iwama. Lo que caracteriz&oacute; la ense&#241;anza de Saito Sensei, fue la misma importancia que le dio a las t&eacute;cnicas de 
	Tai-Jutsu (t&eacute;cnicas de manos libres) y al Buki-Waza (T&eacute;cnicas con armas), que comprende el estudio del Ken (espada) y del Jo (bast&oacute;n). 
	Esta interrelaci&oacute;n entre el Tai-jutsu, el Jo y el Ken era para el fundador, determinante para el estudio del Aikido y Saito Sensei bas&oacute; toda su 
	pedagog&iacute;a en la aplicaci&oacute;n meticulosa de estos principios.
</p>
<p>
	Luego de la muerte de O'Sensei, fue el guardi&aacute;n y protector del Santuario Aiki de Iwama, construido por el propio fundador del Aikido, para la 
	preservaci&oacute;n y el cuidado de la t&eacute;cnica y el esp&iacute;ritu del Aikido para las futuras generaciones. Fue ah&iacute; donde el fundador 
	vivi&oacute; a partir del fin de la segunda guerra mundial y fue ah&iacute; tambi&eacute;n donde finalmente naci&oacute; el t&eacute;rmino AIKIDO, luego que 
	el fundador evolucionara su arte del Aiki-bujutsu, al Aiki-budo, para finalmente llegar al Aikido o la v&iacute;a del Aiki. Por ello este lugar es reconocido 
	como el lugar del nacimiento del Aikido.
</p>
<img src="img/saito.jpg" style="float:left; margin-right: 10px;" />
<p>
	Es tambi&eacute;n uno de los cuatro lugares donde descansan cenizas de O'Sensei, junto con el templo de la Familia Ueshiba en Tanabe, el cementerio de la 
	familia Ueshiba en Ayabe, y el Gran templo y Santuario de Kumano.
</p>
<p>
	El Aikido moderno, tal y como que se practica hoy en d&iacute;a en la mayor parte del mundo, deriva de la interpretaci&oacute;n de famosos maestros como 
	Gozo Shioda, Koichi Tohei, el propio hijo del fundador Kishomaru Ueshiba, Kenji Tomiki, Minoru Mochizuki y tantos otros alumnos del creador del arte y no 
	directamente de la difusi&oacute;n internacional del Aikido por el fundador mismo.
</p>
<p>
	A partir de los a&#241;os 50 estos famosos maestros introdujeron sus propias ideas y modificaciones personales al arte, lo cual era previsible.
</p>
<p>
	La raz&oacute;n de esto se explica f&aacute;cilmente, por los eventos posteriores a la 2<sup>a</sup> Guerra Mundial, que fue el largo periodo de retiro 
	voluntario de O'Sensei en Iwama.
</p>
<p>
	Tambi&eacute;n se debe al hecho que el hijo del fundador, Kishomaru Ueshiba, por razones obvias y de fuerza mayor, en aquel tiempo tuvo que dedicarse de lleno 
	a la ense&#241;anza y la administraci&oacute;n del cuartel general del Aikikai de Tokio (Hombu Dojo), que fuera fundado tambi&eacute;n por el propio creador 
	del Aikido. Hoy en d&iacute;a esta es la sede de la Fundaci&oacute;n Aikikai.
</p>
<img src="img/Morihei-Ueshiba-and-Morihiro-Saito.jpg" style="float:right; margin-left: 10px;" />
<p>
	Casi todos los viejos alumnos que practicaban en Iwama se alejaron de aquel Dojo, muchos durante la guerra, otros un poco despu&eacute;s, ya que la vida en 
	Iwama era muy dura en aquel tiempo, hab&iacute;a carencia de alimentos y era necesario trabajar la tierra para subsistir.
</p>
<p>
	Sin embargo, Saito Sensei estuvo siempre al lado del Fundador en aquellos a&#241;os dif&iacute;ciles, ayud&aacute;ndolo aun en los trabajos m&aacute;s 
	humildes. Si bien es cierto, fue una vida llena de sacrificios, Saito Sensei tuvo as&iacute; la suerte y fortuna de pasar m&aacute;s tiempo que ning&uacute;n 
	otro alumno con el fundador, pues estuvo durante 24 a&#241;os a su servicio.
</p>
<p>
	La raz&oacute;n de la incre&iacute;ble habilidad que alcanz&oacute; Saito Sensei en la pr&aacute;ctica con armas, as&iacute; como de mano libre, radic&oacute; 
	en el hecho de que muy pocas personas tuvieron el privilegio de estudiar tanto tiempo con el fundador y ninguno como &eacute;l permaneci&oacute; junto al 
	maestro hasta el final de sus d&iacute;as.
</p>
<p>
	Debido a la gran impresi&oacute;n que caus&oacute; la difusi&oacute;n de los libros y videos del Maestro Saito, &eacute;l fue invitado constantemente a muchos 
	pa&iacute;ses del mundo a dictar cursos y seminarios, que sirvieron para enriquecer y elevar el nivel del Aikido Mundial.
</p>
<img src="img/morihiroSaito.png" style="float:left; margin-right: 10px;" />
<p>
	El maestro Morihiro Saito falleci&oacute; el 13 de mayo de 2002 a la edad de 74 a&#241;os. El antiguo Dojo de Iwama se encuentra bajo control de su heredero 
	legal, el Doshu Moriteru Ueshiba, nieto del fundador, quien preside la fundaci&oacute;n Aikikai.
</p>
<p>
	Afortunadamente, la pr&aacute;ctica del Aikido del fundador en Iwama seguir&aacute; adelante en manos del sucesor del maestro Morihiro Saito, su propio hijo 
	<strong>Hitohiro Saito</strong>, quien para esto ha creado la organizaci&oacute;n <strong>"Iwama Shinshin Aiki Shurenkai"</strong>. Actualmente las 
	pr&aacute;cticas siguen como de costumbre en el Tanrenkan de Iwama Jap&oacute;n.
</p>
