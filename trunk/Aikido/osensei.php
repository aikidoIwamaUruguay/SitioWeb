<h1>O Sensei Morihei Ueshiba</h1>

<img src="img/Morihei-Ueshiba130x160.jpg" style="float:left; margin-right: 10px;" />
<p>
	Morihei Ueshiba naci&oacute; el 14 de Diciembre de 1883 en Tanabe, Prefectura de Wakayama. Era el cuarto hijo y el &uacute;nico 
	var&oacute;n de Yoroku Ueshiba y Yuki Itokawa.
</p>
<p>
	En 1902 Morihei se fue a Tokio donde trabaj&oacute; como empleado durante alg&uacute;n tiempo antes de instalar su propia 
	compa&#241;&iacute;a de art&iacute;culos de escritorio y material escolar, el Almac&eacute;n Ueshiba. Fue durante esta primera 
	estancia en Tokio cuando Morihei comenz&oacute; su estudio de las artes marciales, aprendiendo jujutsu y kenjutsu tradicionales. 
	Sin embargo m&aacute;s tarde en ese mismo a&#241;o, enferm&oacute; de beri beri y se vio forzado a abandonar Tokio. 
</p>
<img src="img/osenseiejercito.jpg" style="float:right; margin-left: 10px;" /> 
<p>
	En 1903 Morihei se alist&oacute; en el ejercito en Osaka, donde le pusieron el apodo de "Rey de los soldados" por su habilidad 
	con la bayoneta y su car&aacute;cter honesto y trabajador. Durante la vida militar en sus ratos libres Morihei continuaba con su
	inter&eacute;s en las artes marciales asistiendo al dojo de Masakatsu Nakai en Sakai, donde aprendi&oacute; las t&eacute;cnicas 
	de la escuela Goto Yagyu-ryu jujutsu.
</p>
<p>
	En 1907 Morihei se licenci&oacute; del ej&eacute;rcito y volvi&oacute; a Tanabe, donde trabaj&oacute; en la granja familiar y 
	particip&oacute; de la pol&iacute;tica del pueblo, convirti&eacute;ndose en el l&iacute;der local de la Asociaci&oacute;n de 
	J&oacute;venes. 
</p>
<p>
	Durante este per&iacute;odo su padre contrat&oacute; al judoka Kiyoichi Takagi, que estaba de visita en Tanabe, para 
	ense&#241;ar a Morihei, y convirti&oacute; el granero familiar en un dojo. Aqu&iacute; fue donde Morihei aprendi&oacute; el 
	estilo de judo del Kodokan. Tambi&eacute;n continu&oacute; asistiendo al Nakai Dojo y recibi&oacute; un certificado de la 
	escuela Goto. 
</p>
<p>
	Morihei permaneci&oacute; en Tanabe los tres a&#241;os siguientes. En 1910, se interes&oacute; en un plan gubernamental de 
	colonizaci&oacute;n de Hokkaido. Decidi&oacute; formar un grupo de colonos convirti&eacute;ndose en el l&iacute;der del grupo 
	Kishu y en Marzo de 1912 abandon&oacute; Tanabe.
</p>
<img src="img/morihei-ueshiba-1918.jpg" style="float:left; margin-right: 10px;" />
<p>
	Esta zona donde todav&iacute;a hoy existe el pueblo de Shirataki, era entonces un erial, y los colonizadores tuvieron que luchar 
	contra el clima inclemente y las pobres condiciones de la tierra. Fue durante su per&iacute;odo en Hokkaido cuando Morihei 
	conoci&oacute; a Sokaku Takeda, el famoso maestro de la Daito-ryu jujutsu. 
</p>
<p>
	A mediados de Noviembre de 1919 Morihei recibi&oacute; la noticia de que su padre estaba seriamente enfermo. Abandon&oacute; 
	Hokkaido para dirigirse a Tanabe, cerrando un per&iacute;odo de ocho a&#241;os en Shirataki. En su viaje de regreso, Morihei 
	oy&oacute; que el l&iacute;der de la religi&oacute;n Omoto-kyo, Onisaburo Deguchi, estaba viviendo en la cercana Ayabe. 
</p> 

<p>
	Luego de la muerte de su padre el 2 de Enero de 1920 Morihei decidi&oacute; trasladarse a Ayabe en busca de una vida m&aacute;s 
	espiritual bajo la direcci&oacute;n de Onisaburo Deguchi. Obtuvo una casa detr&aacute;s de la escuela primaria, dentro del 
	recinto sagrado de la Omoto-kyo y all&iacute; vivi&oacute; ocho a&#241;os hasta su traslado a Tokio en 1928. 
</p>
<p>
	Durante este per&iacute;odo Morihei convirti&oacute; parte de su casa en un dojo de dieciocho tatami, y abri&oacute; la Academia 
	Ueshiba, donde ense&#241;aba cursos introductorios en las artes marciales, principalmente a seguidores de la Omoto-kyo. 
</p> 
<p>
	Al a&#241;o siguiente del traslado de Morihei a Ayabe, la instrucci&oacute;n que se ofrec&iacute;a en la Academia Ueshiba 
	empez&oacute; gradualmente a aumentar en extensi&oacute;n y en profundidad, y comenz&oacute; a correr la voz de que hab&iacute;a
	un maestro excepcional de artes marciales viviendo en Ayabe.
</p>
<img src="img/Onisaburo.jpg"  style="float:right; margin-left: 10px;" /> 
<p>
	En torno a esta &eacute;poca la pr&aacute;ctica de Morihei de las artes marciales comenz&oacute; gradualmente a adquirir un 
	car&aacute;cter espiritual. Esta concepci&oacute;n fue llamada formalmente en 1922 "aiki-bujutsu", pero se dio a conocer al 
	p&uacute;blico en general como Ueshiba- ryu aiki-bujutsu. En 1924 Morihei se embarc&oacute; en una aventura que result&oacute; 
	ser crucial para su desarrollo espiritual. El 13 de Febrero se fue en secreto de Ayabe con Onisaburo en direcci&oacute;n a 
	Manchuria y a Mongolia, en busca de una tierra sagrada donde poder establecer un nuevo gobierno mundial basado en preceptos 
	religiosos. El d&iacute;a 15 llegaron a Mukden y condujeron al Ej&eacute;rcito Aut&oacute;nomo del Noroeste (tambi&eacute;n 
	conocido como el Ej&eacute;rcito de la Independencia de Mongolia), al interior del pa&iacute;s. Sin embargo, su 
	expedici&oacute;n fue desafortunada; cayeron v&iacute;ctimas de un complot y el 20 de Junio, se encontraron con tropas chinas 
	esper&aacute;ndoles para arrestarles. Morihei, Onisaburo y cuatro m&aacute;s fueron sentenciados a muerte. Afortunadamente, 
	justo antes de que les fueran a ajusticiar, un miembro del personal consular japon&eacute;s intervino y consigui&oacute; su 
	liberaci&oacute;n y su retorno a Jap&oacute;n.
</p>
<p>
	En 1930 Morihei consigui&oacute; una villa en Ushigome, Wakamatsu-ch&oacute;, y comenz&oacute; la construcci&oacute;n de un 
	nuevo dojo. Mientras se estaba realizando el trabajo dispuso de un dojo temporal en Mejirodai, y fue all&iacute;, en Octubre 
	de 1930, donde recibi&oacute; la visita de Jigoro Kano, el fundador del judo y cabeza del Kodokan. A Kano le impresion&oacute; 
	mucho la t&eacute;cnica de Morihei y le alabo diciendo "Este es mi budo ideal". Posteriormente, Kano envi&oacute; a dos de sus 
	alumnos, Jiro Takeda y Minoru Mochikuzi, para que entrenaran con Morihei. 
</p>
<p>
	En Abril de 1931 finaliz&oacute; la construcci&oacute;n de un dojo de aiki-budo de un total de ochenta tatami, conocido como el 
	Kobukan. Durante los diez a&#241;os siguientes el aiki-budo experiment&oacute; su primera &eacute;poca dorada. En esa 
	&eacute;poca el Kobukan era conocido como el "dojo del infierno" por el entrenamiento tan extraordinariamente intenso que se 
	practicaba all&iacute;. 
</p>
<img src="img/Morihei-UeshibaParado.jpg" style="float:left; margin-right: 10px;" />
<p>
	Para mediados de los a&#241;os treinta, Morihei se hab&iacute;a hecho famoso en el mundo de las artes marciales. Llam&oacute; la 
	atenci&oacute;n del p&uacute;blico por la naturaleza original de su creaci&oacute;n "la uni&oacute;n del esp&iacute;ritu, de la 
	mente y del cuerpo" en el aiki, llamada formalmente aiki-budo. Durante este per&iacute;odo Morihei practicaba kendo 
	intensivamente en el Kobukan Dojo.
</p>
<p>
	Con el estallido de la Guerra del Pac&iacute;fico los alumnos del dojo de Tokio fueron y&eacute;ndose uno tras otro al frente. 
	En 1941, el aiki budo fue incorporado al Butokukai (una corporaci&oacute;n gubernamental que reun&iacute;a todas las artes 
	marciales en una sola organizaci&oacute;n). Fue en torno a esta &eacute;poca cuando se empez&oacute; a utilizar por primera vez 
	el nombre de aikido. Morihei decide restablecer las bases organizativas del aikido en la Prefectura de Ibaraki y se 
	traslad&oacute; con su mujer a Iwama, donde vivi&oacute; frugalmente en una granja.
</p>
<img src="img/iwama220.jpg"  style="float:right; margin-left: 10px;" />
<p>
	En Iwama, Morihei comenz&oacute; la construcci&oacute;n del santuario interior del aikido: un complejo que inclu&iacute;a el 
	Santuario Aiki y un dojo aire libre. 
</p>
<p>
	Luego de finalizada la guerra, el Dojo Kobukan en Tokio sali&oacute; indemne, pero despu&eacute;s de la guerra fue utilizado 
	como refugio por m&aacute;s de treinta familias sin hogar, siendo imposible continuar all&iacute; con la pr&aacute;ctica. Por 
	tanto se traslado la sede central del aikido a Iwama. 
</p>
<p>
	El 9 de Febrero de 1948 el Ministerio de Educaci&oacute;n concedi&oacute; permiso para restablecer el Aikikai. Durante 
	esa &eacute;poca el dojo principal de Tokio recibi&oacute; el nuevo nombre de Ueshiba Dojo y Sede Central Mundial del Aikido.
</p>
<p>
	De 1950 en adelante, Morihei comenz&oacute; de nuevo a viajar por el Jap&oacute;n respondiendo a invitaciones para ense&#241;ar, 
	dando conferencias y haciendo demostraciones. Cuando alcanz&oacute; los setenta a&#241;os de edad, la magnifica t&eacute;cnica 
	de Morihei surg&iacute;a cada vez con m&aacute;s fluidez de la inmensidad de su esp&iacute;ritu, en contraste con la fiereza y 
	la fuerza f&iacute;sica caracter&iacute;sticas de sus primeros a&#241;os poniendo un mayor &eacute;nfasis en la naturaleza 
	amorosa del aikido (El primer car&aacute;cter de aikido, "ai", que significa armon&iacute;a, se lee de la misma manera que el 
	car&aacute;cter que significa amor. En sus &uacute;ltimos a&#241;os, Morihei acentuaba la equivalencia de estos dos significados)
</p>
<img src="img/osenseicolor.jpg" style="float:left; margin-right: 10px;" />
<p>
	Cuando el aikido se fue estableciendo en la conciencia popular, aument&oacute; r&aacute;pidamente el n&uacute;mero de alumnos de 
	todas partes del mundo. En el propio Jap&oacute;n se abrieron nuevos dojos a lo largo de todo el pa&iacute;s, y el aikido se 
	extendi&oacute; a las universidades, a las oficinas gubernamentales y a las empresas, anunciando una segunda &eacute;poca dorada.
</p>
<p>
	En 1960, Morihei recibi&oacute; de manos del Emperador Hirohito, junto con Yosaburo Uno, d&eacute;cimo dan de Kyudo, la 
	Condecoraci&oacute;n Shijuhosho. El 28 de Febrero de 1961 Morihei fue a los Estados Unidos, invitado por el Aikikai de Hawai. 
	Durante su visita, el fundador hizo la siguiente declaraci&oacute;n. 
</p>
<p>
	..." He venido a Hawai para construir un "puente de plata". Hasta ahora he permanecido en Jap&oacute;n construyendo un "puente 
	de oro" para unir a Jap&oacute;n, pero de ahora en adelante deseo construir un puente para unir a los diferentes pa&iacute;ses 
	del mundo a trav&eacute;s de la armon&iacute;a y del amor contenidos en el aikido. Pienso que el aiki, fruto de las artes 
	marciales, puede unir a las gentes del mundo en armon&iacute;a, en el verdadero esp&iacute;ritu del budo, envolviendo al mundo 
	en un amor inalterable"...
</p>
<img src="img/MoriheiUeshiba1.jpg"  style="float:right; margin-left: 10px;" />
<p>
	En 1964 recibi&oacute; una condecoraci&oacute;n especial del Emperador Hirohito en reconocimiento a su contribuci&oacute;n a las 
	artes marciales. El 14 de Marzo de 1967 se llev&oacute; a cabo la ceremonia de iniciaci&oacute;n de los trabajos para la 
	construcci&oacute;n del nuevo Hombu Dojo en Tokio. El 15 de diciembre de ese a&#241;o se termin&oacute; el nuevo dojo, un 
	moderno edificio de hormig&oacute;n de tres pisos.
</p> 
<p>
	El 15 de Enero de 1969, Morihei asisti&oacute; en el Hombu Dojo a las celebraciones del A&#241;o Nuevo. Aunque parec&iacute;a 
	gozar de buena salud, su condici&oacute;n f&iacute;sica se deterior&oacute; r&aacute;pidamente y muri&oacute; pl&aacute;cidamente
	el 26 de Abril de 1969 a las 5 de la tarde. El 1 de Mayo, a partir de las 7:10 de la tarde, se celebr&oacute; una vigilia en el 
	Hombu Dojo, y ese mismo d&iacute;a el Emperador Hirohito le concedi&oacute; una condecoraci&oacute;n p&oacute;stuma. Sus cenizas 
	fueron enterradas en el templo de la familia Ueshiba en el cementerio de Tanabe, y se guardaron mechones del cabello del fundador
	en el santuario Aiki de Iwama, en el cementerio de la familia Ueshiba en Ayabe y en el Gran Santuario Kumano.
</p>
