<h1>Hitohira Saito</h1>

<img src="img/hitohira-saito130x160.jpg" style="float:left; margin-right: 10px;" />
<p>
	Hitohira Saito naci&oacute; el 12 de febrero 1957, en Iwama. Es el tercer hijo y el &uacute;nico var&oacute;n de Morihiro Saito Sensei y de Sata Saito.
</p>
<p>
	Cuando naci&oacute;, sus padres estaban demasiado involucrados sirviendo al fundador y su familia, tanto en el Aikido, como en la agricultura. Por lo tanto, 
	era raro ver a sus padres en sus primeros a&#241;os de vida. Sus hermanas mayores se hicieron cargo de &eacute;l y le ayudaban a diario. Desde el amanecer, 
	cuando se despertaba, ya sus padres hab&iacute;an dejado la casa para servir a O'Sensei. El &uacute;nico rastro que quedaba de ellos en su casa, era el 
	almuerzo para llevar a la escuela que su madre le preparaba antes del amanecer.
</p>
<p>
	A la edad de siete a&#241;os O'Sensei le permite comenzar a entrenar Aikido. El entrenamiento vigoroso y riguroso que lleva a partir de entonces, lo 
	convertir&aacute;n en uno de los practicantes m&aacute;s eficaces del mundo. Se entren&oacute; con el fundador hasta que este se retiro a Tokio 
	definitivamente en los &uacute;ltimos meses de su vida. Despu&eacute;s continu&oacute; entrenando con su padre, Morihiro Saito Sensei por m&aacute;s de 
	treinta a&#241;os.
</p>
<p>
	Ten&iacute;a nueve a&#241;os cuando se recuerda instruir durante el jiyu keiko (entrenamientos libres despu&eacute;s de la clase de aikido), a los cinturones 
	negros que ven&iacute;an de Tokio que, por nunca entrenar con el Fundador, no ten&iacute;an la habilidad de seguir y repetir su t&eacute;cnica.
</p>
<img src="img/hitohiro2.jpeg" style="float:right; margin-left: 10px;" />
<p>
	Hitohira Saito Shihan, estudi&oacute; cocina en Osaka y tambi&eacute;n en la ciudad de Sendai. All&iacute; tambi&eacute;n pudo entrenar con grandes maestros 
	como Seiseki  Abe Shihan en Osaka y con Hansawa Shihan en Sendai. Seiseki Abe (1915 - 18 Mayo de 2011) fue el maestro de caligraf&iacute;a del fundador del 
	Aikido  y tambi&eacute;n ense&#241;o caligraf&iacute;a a Hitohira Sensei.
</p>
<p>
	Al terminar sus estudios regresa a Iwama y abre un restaurante en frente al templo de Aikido, en el terreno que el fundador dio a la familia Saito. M&aacute;s
	tarde, tubo que dejar esta profesi&oacute;n debido a que su padre le pidi&oacute; que se dedicara m&aacute;s tiempo al Aikido.
</p>
<p>
	Adem&aacute;s de ser un excelente cal&iacute;grafo y pintor, es tambi&eacute;n un escultor tradicional. Sus m&aacute;scaras y cabezas de leones Shishi 
	utilizadas en festivales tradicionales japoneses, son muy famosos.
</p>
<p>
	En 2000, empujado por su padre construye su dojo privado. Se inaugur&oacute; en presencia de su padre, Morihiro Saito, el 16 de julio de 2000. El nuevo dojo 
	lleva el nombre de <strong>Tanrenkan</strong>.
</p>
<p>
	A&#241;os antes del fallecimiento de su padre, Hitohira Saito se encarg&oacute; del dojo del Fundador, para facilitar el trabajo de su padre y de su madre. 
	Con la muerte de Morihiro Saito Sensei, Hitohira continu&oacute; gobernando el dojo del Fundador y el templo del Aikido, sirviendo a la familia Ueshiba como 
	siempre. Sin embargo, las divergencias con el Aikikai aumentan, cuando queda claro que se trata de evitar que Hitohira Sensei contin&uacute;e ense&#241;ando 
	Aikido en el dojo del Fundador. Estas diferencias llevaron a la separaci&oacute;n con la organizaci&oacute;n del Aikikai en febrero de 2004 y a formar su 
	propia organizaci&oacute;n llamada <strong>Iwama Shin Shin Aikishurenkai</strong>.
</p>
<p>
	Muchos de los estudiantes de su padre, especialmente los j&oacute;venes, le siguen y Hitohira Sensei monta su Honbu Dojo (dojo central) en el Tanrenkan.
</p>
<p>
	Hitohira Sensei como su padre contin&uacute;a viajando por el mundo tratando de responder a numerosas solicitudes de seminarios internacionales. En 
	Jap&oacute;n, tambi&eacute;n viaja constantemente para ense&#241;ar, de norte a sur.
 </p>
